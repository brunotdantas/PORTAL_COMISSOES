SELECT DISTINCT 
	LTRIM(RTRIM(F2_DOC))+LTRIM(RTRIM(F2_SERIE)) AS numVenda,
	F2_VEND1	AS idVendedor,
	A3_NOME		as NomeVendedor,
	F2_VALBRUT,
	F2_FILIAL AS idLoja
FROM SF2020 WITH (NOLOCK)
INNER JOIN SD2020 WITH (NOLOCK) ON D2_FILIAL = F2_FILIAL AND D2_DOC = F2_DOC AND D2_SERIE = F2_SERIE
INNER JOIN SA3020 WITH (NOLOCK) ON A3_COD = F2_VEND1
WHERE F2_EMISSAO BETWEEN '20180101' AND '20180631'
AND F2_FILIAL IN (
'59',
'45',
'25',
'37',
'78',
'04',
'70',
'66',
'60',
'48'
)
ORDER BY 5


